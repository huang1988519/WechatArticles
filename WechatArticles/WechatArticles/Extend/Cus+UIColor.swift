import UIKit

extension UIColor {
    class  func cus_RedColor() -> UIColor {
        return  UIColor(red: 232.0/255.00, green: 87.0/255.00, blue: 100.00/255.00, alpha: 1)
    }
    class func cus_GreenColor() -> UIColor {
        return UIColor(hex: "2EA2C2")
    }
}
